// 半角カタカナ→全角カタカナ マッピング（ZEN_TO_HAN_KANA の逆）
const HAN_TO_ZEN_KANA: Record<string, string> = {};
for (const [zen, han] of Object.entries(ZEN_TO_HAN_KANA)) {
  // 濁音・半濁音は後で組み合わせ処理するためスキップ
  if (!HAN_TO_ZEN_KANA[han]) {
    HAN_TO_ZEN_KANA[han] = zen;
  }
}

// 濁音・半濁音 結合マッピング（半角2文字→全角1文字）
const HAN_DAKUTEN: Record<string, string> = {
  'ｶﾞ': 'ガ', 'ｷﾞ': 'ギ', 'ｸﾞ': 'グ', 'ｹﾞ': 'ゲ', 'ｺﾞ': 'ゴ',
  'ｻﾞ': 'ザ', 'ｼﾞ': 'ジ', 'ｽﾞ': 'ズ', 'ｾﾞ': 'ゼ', 'ｿﾞ': 'ゾ',
  'ﾀﾞ': 'ダ', 'ﾁﾞ': 'ヂ', 'ﾂﾞ': 'ヅ', 'ﾃﾞ': 'デ', 'ﾄﾞ': 'ド',
  'ﾊﾞ': 'バ', 'ﾋﾞ': 'ビ', 'ﾌﾞ': 'ブ', 'ﾍﾞ': 'ベ', 'ﾎﾞ': 'ボ',
  'ﾊﾟ': 'パ', 'ﾋﾟ': 'ピ', 'ﾌﾟ': 'プ', 'ﾍﾟ': 'ペ', 'ﾎﾟ': 'ポ',
  'ｳﾞ': 'ヴ',
};

export function toHankaku(input: string): string {
  return input
    // 全角ASCII記号・英数字 → 半角 (U+FF01-FF5E → U+0021-007E)
    .replace(/[\uFF01-\uFF5E]/g, (ch) => String.fromCharCode(ch.charCodeAt(0) - 0xFEE0))
    // 全角スペース → 半角スペース
    .replace(/\u3000/g, ' ')
    // 全角カタカナ・句読点 → 半角 (、=U+3001 から ー=U+30FC の範囲)
    .replace(/[ァ-ヶｦ-ﾟ、-ー]/g, (ch) => ZEN_TO_HAN_KANA[ch] ?? ch);
}

export function toZenkaku(input: string): string {
  // 半角カタカナ・句読点（濁音・半濁音の2文字組み合わせを先に処理）
  // ｡=U+FF61 から ﾟ=U+FF9F の範囲で全半角句読点含む
  const result = input.replace(/[｡-ﾟ][ﾞﾟ]|[｡-ﾟ]/g, (ch) => HAN_DAKUTEN[ch] ?? HAN_TO_ZEN_KANA[ch] ?? ch);

  return result
    // 半角ASCII記号・英数字 → 全角 (U+0021-007E → U+FF01-FF5E)
    .replace(/[\u0021-\u007E]/g, (ch) => String.fromCharCode(ch.charCodeAt(0) + 0xFEE0))
    // 半角スペース → 全角スペース
    .replace(/ /g, '\u3000');
}
